public class OperadoresUnarios {

    public static void main(String[] args) {

        int i = -5 ;

        System.out.println("-----------------------------------------------");

        int j = +1 ; // j = (1)*1 => -5
        System.out.println("j = " + j);

        int k = -i ;// k = (-1) * => 5
        System.out.println("k = " + k);

        System.out.println("------------------------------------------------");

        i = 6 ;
        j = +i ;
        System.out.println("j = " + j);

        k = -i ;

        System.out.println("k = " + k);

        System.out.println("----------------------------------------------");


    }

}
